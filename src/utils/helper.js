export function generateRandomMessage(minWords = 2, maxWords = 10) {
  const wordCount =
    Math.floor(Math.random() * (maxWords - minWords + 1)) + minWords;
  const shuffled = [...words].sort(() => 0.5 - Math.random());
  const selected = shuffled.slice(0, wordCount);

  // Capitalize first letter and add period
  const sentence =
    selected.join(" ").charAt(0).toUpperCase() + selected.join(" ").slice(1);
  return sentence;
}

// Find nth Prime
export const findPrime = (num) => {
  let i,
    primes = [2, 3],
    n = 5;
  const isPrime = (n) => {
    let i = 1,
      p = primes[i],
      limit = Math.ceil(Math.sqrt(n));
    while (p <= limit) {
      if (n % p === 0) {
        return false;
      }
      i += 1;
      p = primes[i];
    }
    return true;
  };
  for (i = 2; i <= num; i += 1) {
    while (!isPrime(n)) {
      n += 2;
    }
    primes.push(n);
    n += 2;
  }
  return primes[num - 1];
};
